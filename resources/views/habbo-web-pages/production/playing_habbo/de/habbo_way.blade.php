<h1>Habbo Benimmregeln</h1>
<p>Die Habbo Benimmregeln sind wie Verhaltensregeln die den Habbos zeigen, wie sie sich im Hotel benehmen sollen. Habbo
    macht viel mehr Spa&#xDF;, wenn man sich an die Regeln h&#xE4;lt!</p>
<h2>IN ORDNUNG&#x2026;</h2>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/habboway_2a.png" alt="In Ordnung Chat" class="align-right"></p>
<h4>Chat</h4>
<p>Sei eine Plaudertasche und lern deine Habbofreunde kennen! Finde neue Freunde, chatte mit deinen alten Freunden und
    hab SPA&#xDF;!</p>
<h4>Erstellen</h4>
<p>Lass deiner Kreativit&#xE4;t freien Lauf und hol alles aus dir raus! Baue tolle R&#xE4;ume und entwirf coole Selfies
    - aus dir wird vielleicht der neue Pixel Picasso!</p>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/habboway_5a.png" alt="In Ordnung Erstellen" class="align-right">
</p>
<h4>Hilfe</h4>
<p>Hilf einem Fremden, gewinne einen Freund! Sei immer hilfsbereit gegen&#xFC;ber anderen Habbos - du weisst nie, ob du
    eines Tages selbst Hilfe brauchst.</p>
<h4>Handel</h4>
<p>Erstelle dein eigenes M&#xF6;belimperium und handle dich hoch! Wenn du ein Gesp&#xFC;r f&#xFC;rs Gesch&#xE4;ft hast,
    dann nutze den Flohmarkt um M&#xF6;bel zu verkaufen und deinen Talervorrat aufzustocken.</p>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/habboway_1a.png" alt="In Ordnung spiele Games"
        class="align-right"></p>
<h4>Spiele und erstelle Games</h4>
<p>Spiel mit Freunden, und bring deinen Kampfgeist zum Ausdruck! Oder erstelle deine eigenen Spiele und werde beliebt!
    Erfolgreiche Spiele bedeuten, andere Habbos m&#xF6;chten an deinen Spielen teilnehmen um Spa&#xDF; zu haben.</p>
<h4>Finde Freunde</h4>
<p>Hab Spa&#xDF;, halte dich hier auf und vielleicht findest einen guten Pixel Freund&#x2026;</p>
