<h2>LIEBER NICHT&#x2026;</h2>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/habboway_2b.png" alt="Lieber nicht Troll" class="align-right">
</p>
<h4>Troll</h4>
<p>Keiner mag Trolle, nicht mal ihre M&#xFC;tter. Bel&#xE4;stigungen werden von niemandem akzeptiert.</p>
<h4>Scam oder Skript</h4>
<p>Originale sind besser! Niemand mag Betr&#xFC;ger. Stehlen macht einen nicht reich, sondern kriminell. Und zum denkbar
    schlechten Vorbild.</p>
<h4>Streich oder Cheat</h4>
<p>Cheater haben nie Erfolg, sie verderben nur den anderen den Spa&#xDF;.</p>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/habboway_7b.png"
        alt="Lieber nicht F&#xFC;r echtes Geld verkaufen" class="align-right"></p>
<h4>F&#xFC;r echtes Geld verkaufen</h4>
<p>Verkaufe weder deine M&#xF6;bel, noch deinen Account f&#xFC;r echtes Geld. Das Risiko alles an einem nicht sicheren
    Ort zu verlieren ist gro&#xDF; und es lohnt sich nicht die Zeit und die M&#xFC;he, die du investiert hast, um
    dorthin zu kommen, wo du nun bist, aufs Spiel zu setzen.</p>
<h4>Wetten abschlie&#xDF;en oder akzeptieren</h4>
<p>Die Verwendung von M&#xF6;beln, die nach dem Zufallsprinzip funktionieren, f&#xFC;r Wetten kann dir Probleme bringen.
    Zeige dein K&#xF6;nnen, &#xFC;berlass es nicht dem Zufall!</p>
<p><img src="{{$chocolatey['url']}}habbo-web/assets/web-images/habboway_3b.png" alt="Lieber nicht Cyber" class="align-right">
</p>
<h4>Cyber</h4>
<p>Cybering ist streng verboten, Webcamanfragen haben Strafen zur Folge. Triff dich nicht mit jemandem, den du nur aus
    dem Internet kennst, Menschen sind nicht immer, wer sie vorgeben zu sein.</p>
