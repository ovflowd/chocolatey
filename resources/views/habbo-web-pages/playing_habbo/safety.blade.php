<p>You have no control over your personal photos, videos + webcam images after you share them on the internet. Once an image is posted, it can never be removed, will be viewable by anyone and could be used to bully or blackmail you. Before you share a pic or video, ask yourself; are you comfortable with people you don&apos;t know viewing it?</p>
