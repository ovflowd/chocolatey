Builder’s Club Membership
<span class="padding-top"><br></span><h1>Builder’s Club Membership Information </h1><span
        class="padding-top"><br></span>
<span class="padding-top"><br></span><h2>What is Builder’s Club? <br>
    Builder’s Club is a new feature which which gives you a virtual warehouse full of furni. You can build rooms without
    worrying about how much credits you'll need to complete a project. If something doesn't look good in your room, put
    it back and grab something else. Its that easy! </h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Free Trial.<br>
    All Habbos have a free trial automatically. The free trial allows you to build rooms with a basic limit of furni.
    Membership is required to unlock your Builder’s Club rooms for public access. </h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Membership When you become a member, your rooms made with Builder’s Club will
    automatically unlock and allow you to select from the usual types of access (open, private, invisible, and locked).
    You will also receive a <b>permanent</b> increase to your furni limit! Membership status and current furniture limit
    are shown in the Builder’s Club Warehouse </h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Using BC Furniture <br>
    *To use BC furniture, drag it from the Builder’s Club Warehouse into your room </h2><span
        class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p class="bullet-item">·&nbsp;To return BC furniture back to the Builder’s Club
    Warehouse, simply pick it up </p>
<p class="bullet-item">·&nbsp;You can use BC furniture in your own rooms as well as in group home rooms </p>
<span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Floor plan editor <br>
    The floor plan editor lets you easily create custom floor plans, as well as the ability to move doorways, change
    wall height setting and change the direction Habbos face upon entry. Display the editor in the room that you want to
    edit by typing :floor or :bcfloor. </h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Furni selection<br>
    Builder’s Club has a wide selection of decorative furni lines for use in your designs. This includes many of the
    permanent lines from our catalogue as well as campaign and seasonal items which come and go. </h2><span
        class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Furni limit </h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p class="bullet-item">·&nbsp;You can only have certain number of BC furniture in
    your rooms. New users start with a Basic Limit 7</p>
<p class="bullet-item">·&nbsp;When you become a member you get a <b>permanent</b> boost to the furniture limit (up to
    1000). </p>
<p class="bullet-item">·&nbsp;After this, you will receive an additional +250 to your furni limit every time you buy a
    month of membership. </p>
<p class="bullet-item">·&nbsp;Further <b>permanent</b> furni limit upgrades you buy are on top of those which are
    included as additions when purchasing a month of membership. </p>
<span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Expired Membership <br>
    If your membership ends, your rooms and furni limit will remain intact. The rooms made with Builder’s Club furni
    will simply become locked for visitors, but you can continue to enter and work on them by yourself. Your furni limit
    will not reset and if you choose to become a member again the rooms will unlock automatically.</h2><span
        class="padding-bottom"><br></span>