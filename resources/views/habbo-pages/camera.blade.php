Habbo Camera
<span class="padding-top"><br></span><h3>How does the camera work?</h3><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>The camera icon is to be found on the bottom left corner of your client screen.
    By clicking the icon you'll get your camera ready for taking photos. Photo taking is easy, just press the red
    button! The photos taken (max. 5) are saved in the menu below the camera. You can also replace the saved photos with
    the ones you like the best. Just bare in mind that the saved photos are not permanent but disappear when reloading
    the client. The only way to keep the photos to yourself is converting them into wall-item furnis.</p><span
        class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h3>Is the camera function available for all the users?</h3><span
        class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>Yes it is, everyone can try taking photos! Taking photos is free and at the
    reach of all the users, while converting them into wall-item furnis costs 2 credits per photo.</p><span
        class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h3>Are the photos being moderated?</h3><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>Yes. Inappropriate photos can be reported to the moderators by clicking the red
    flag icon below the photo wall items. Moderators can remove the photos not respecting the Habbo Way in which case
    the price won't be refunded. However, each case reported is studied before moderator action is taken.</p><span
        class="padding-bottom"><br></span>
